  test:
    runs-on: ubuntu-latest
    
    steps:
    - uses: actions/checkout@v3
    
    - name: Set up Python
      uses: actions/setup-python@v4
      with:
        python-version: '3.9'
        
    - name: Install dependencies
      run: |
        python -m pip install --upgrade pip
        pip install -r requirements.txt
        
    - name: Test StealtHub AI
      run: |
        python ai_engine/stealth_hub_ai_engine.py
        python stealth_hub_cli.py --version
        
    - name: Test Code Generation
      run: |
        python stealth_hub_cli.py --request "test aimbot for Free Fire"
        
  build-mobile:
    runs-on: ubuntu-latest
    needs: test
    
    steps:
    - uses: actions/checkout@v3
    
    - name: Setup Node.js
      uses: actions/setup-node@v3
      with:
        node-version: '18'
        
    - name: Install mobile dependencies
      run: |
        cd mobile
        npm install
        
    - name: Build Android APK
      run: |
        cd mobile
        npm run build:android
        
    - name: Build iOS App
      run: |
        cd mobile
        npm run build:ios

  create-release:
    runs-on: ubuntu-latest
    needs: [test, build-mobile]
    if: github.ref == 'refs/heads/main'
    
    steps:
    - uses: actions/checkout@v3
    
    - name: Create Release
      uses: actions/create-release@v1
      env:
        GITHUB_TOKEN: ${{ secrets.GITHUB_TOKEN }}
      with:
        tag_name: v2.0.0
        release_name: StealtHub AI v2.0.0
        draft: false
        prerelease: false
        
    - name: Upload Release Assets
      uses: actions/upload-release-asset@v1
      env:
        GITHUB_TOKEN: ${{ secrets.GITHUB_TOKEN }}
      with:
        upload_url: ${{ steps.create_release.outputs.upload_url }}
        asset_path: ./mobile/app-debug.apk
        asset_name: StealtHub-AI-Android-v2.0.0.apk
        asset_content_type: application/vnd.android.package-archive
